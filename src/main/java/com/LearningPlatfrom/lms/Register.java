    String name_,email_,phone_no_,uname,course_id;
    boolean validation(){
        name_=name_input.getText().trim();
        email_=email_input.getText().trim();
        phone_no_=phone_no_input.getText();
        uname=username_input.getText().trim();
        course_id=id.getSelectedItem().toString();
        String regex="^[A-Za-z0-9._%+-]+@gmail.com";
        System.out.println("Selected Course ID: " + course_id);
        if(name_.equals("")){
            JOptionPane.showMessageDialog(this, "Name can't be empty!");
            return false;
        }
        if(name_.length()<=2){
            JOptionPane.showMessageDialog(this, "Name can't be smaller than 3!");
            return false;
        }
        if(email_.equals("")){
            JOptionPane.showMessageDialog(this, "Email can't be empty!");
            return false;
        }
        if(!email_.matches(regex)){
            JOptionPane.showMessageDialog(this, "Invalid email!");
            return false;
        }
        if(phone_no_.length()!=10||phone_no_.startsWith("0")){
            JOptionPane.showMessageDialog(this, "Invaild Phone number!");
            return false;
        }
        char[] arr=phone_no_.toCharArray();
        for(int i=0;i<10;i++){
            if(!(arr[i]>='0'&&arr[i]<='9')){
                JOptionPane.showMessageDialog(this, "Phone number can conatin only numbers!");
                return false;
            }
        }
        if(uname.equals("")){
            JOptionPane.showMessageDialog(this, "Username can't be empty!");
            return false;
        }
        return true;
    }
