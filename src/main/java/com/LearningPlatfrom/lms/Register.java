    void insert_data(){
        if(!validation()){
            return;
        }
        Connection con; boolean f1=false,f2=false;
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/learning?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "");
            String sql="insert into enrolled(username, course_id) values(?,?)";
            PreparedStatement st=con.prepareStatement(sql);
            st.setString(1, uname);
            st.setString(2, course_id);
            int rows = st.executeUpdate();
            if(rows > 0) {
                f1 = true;
            }
            con.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Enrolled insert failed: " + ex.getMessage());
        } 
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/learning?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "");
            String sql = "UPDATE signup SET name=?, email=?, phone_number=? WHERE username=?";
            PreparedStatement st=con.prepareStatement(sql);
            st.setString(1, name_);
            st.setString(2, email_);
            st.setString(3, phone_no_);
            st.setString(4,uname);
            int rows = st.executeUpdate();
            if(rows > 0) {
                f2 = true;
            }
            con.close();;
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Signup insert failed: " + ex.getMessage());
        } 
        if(f1==true&&f2==true){
            JOptionPane.showMessageDialog(this, "Data inserted successfully");
        }
        else{
            JOptionPane.showMessageDialog(this, "Data not inserted");
        }
    }
